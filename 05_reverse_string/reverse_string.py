def reverse_string(phrase):
    """Reverse string,

        >>> reverse_string('awesome')
        'emosewa'

        >>> reverse_string('sauce')
        'ecuas'
    """
    
    return phrase[::-1]

result1 = reverse_string('awesome')
print(result1)  # Output: 'emosewa'

result2 = reverse_string('sauce')
print(result2)  # Output: 'ecuas'


